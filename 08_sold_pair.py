# 8. A salesman sold twice as much pears in the afternoon than in the morning. 
# If he sold 360 kilograms of pears that day,
# how many kilograms did he sell in the morning and how many in the afternoon?

total_sold = 360


# Calculate morning pears
morning = 360 / 3

# Calculate afternoon pears
afternoon = morning * 2

print("Total pears =", total_sold)
print("Total sold pears in Morning =", morning)
print("Total sold pears in the afternoon =", afternoon)
